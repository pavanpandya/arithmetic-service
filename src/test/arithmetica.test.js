const { add } = require("../arithmetica")

test('2 + 3 is equal to 5', () => {
    expect(add(2, 3)).toBe(5);
});

test('2099999999999999999 + 30000000000000 is equal to 2100030000000000000', () => {
    expect(add(2099999999999999999, 30000000000000)).toBe(2100030000000000000);
});

test('85 + 73 is equal to 158', () => {
    expect(add(85, 73)).toBe(158);
});

test('123456 + 65321 is equal to 188777 ', () => {
    expect(add(123456, 65321)).toBe(188777);
})

test('10000000000000000000000000 + 123143152526 is equal to 1.0000000000000123e+25 ', () => {
    expect(add(10000000000000000000000000, 123143152526)).toBe(1.0000000000000123e+25);
})